// Traduzioni per il backend (tutto in italiano)
const TRADUZIONI_IT = {
  en: {
    "First Name": "Nome", "Last Name": "Cognome", "Pickup Date": "Data ritiro",
    "Pickup Time": "Ora ritiro", "Number of bikes": "Numero bici",
    "Preferred route": "Percorso", "Special requests": "Note",
    "Booking sent": "Prenotazione inviata"
  }
};

let linguaCorrente = 'it';

function rilevaLingua() {
  const lang = navigator.language || navigator.userLanguage || 'it';
  const codice = lang.substring(0, 2).toLowerCase();
  return TRADUZIONI[codice] ? codice : 'it';
}

function t(chiave) {
  return TRADUZIONI[linguaCorrente][chiave] || TRADUZIONI['it'][chiave] || chiave;
}

function cambiaLingua(lang) {
  linguaCorrente = lang;
  localStorage.setItem('lingua', lang);
  applicaTraduzioni();
  document.querySelectorAll('.lang-btn').forEach(b => {
    b.classList.toggle('attivo', b.dataset.lang === lang);
  });
}

function applicaTraduzioni() {
  // Testo semplice
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const trad = t(el.dataset.i18n);
    el.textContent = trad;
  });
  // HTML (con tag <span>, <strong> ecc.)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
  // Placeholder degli input/textarea
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  // Aggiorna l'attributo lang dell'html
  document.documentElement.lang = linguaCorrente;
}

function iniziaI18n() {
  const saved = localStorage.getItem('lingua');
  linguaCorrente = saved || rilevaLingua();
  applicaTraduzioni();
  document.querySelectorAll('.lang-btn').forEach(b => {
    b.classList.toggle('attivo', b.dataset.lang === linguaCorrente);
  });
}

// Traduci i dati del form in italiano prima di inviarli
function traduciFormInItaliano(data) {
  return {
    ...data,
    _lingua_originale: linguaCorrente,
    _nota_traduzione: linguaCorrente !== 'it' ? `[Prenotazione ricevuta in ${linguaCorrente.toUpperCase()} – tradotta automaticamente]` : ''
  };
}
